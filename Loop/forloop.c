#include <stdio.h>

int main() {
    // for loop to print "Hi" 5 times
    for (int i = 1; i <= 5; i=i+1) {
        printf("Hi\n");
    }

    return 0;
}
// Here, "int i=1" is initialisation, "i<=5" is condition, "i++" or "i=i+1" is updation
//we can also use i=i+1 instead of i++
// we can also use i=i+2 or i=i+3 for that Hi will print adding with the upgradation upto the time i becomes less than or equal to 5